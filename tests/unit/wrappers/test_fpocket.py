class TestInfoTxtParser:
    """The headline parser: turn fpocket's info.txt into per-pocket
    descriptor dicts. These tests use real-shape fpocket output (the
    exact format documented in fpocket's GETTINGSTARTED.md)."""

    def test_parses_three_pockets(self) -> None:
        result = _parse_info_txt(_REAL_FPOCKET_INFO_BLOCK)
        assert len(result) == 3

    def test_extracts_headline_descriptors(self) -> None:
        result = _parse_info_txt(_REAL_FPOCKET_INFO_BLOCK)
        first = result[0]
        # The three molforge cares most about.
        assert first["Score"] == "0.490"
        assert first["Druggability Score"] == "0.019"
        assert first["Volume"] == "270.934"
        # A few of the extras that go into metadata["descriptors"].
        assert first["Number of Alpha Spheres"] == "21"
        assert first["Polar SASA"] == "7.611"

    def test_handles_pockets_with_fewer_descriptors(self) -> None:
        # Pocket 3 in the fixture has only 4 descriptors; the parser
        # shouldn't require any particular set to be present.
        result = _parse_info_txt(_REAL_FPOCKET_INFO_BLOCK)
        third = result[2]
        assert "Score" in third
        assert "Volume" in third
        assert "Polar SASA" not in third  # absent in this block

    def test_empty_input_returns_empty_list(self) -> None:
        assert _parse_info_txt("") == []
        assert _parse_info_txt("   \n\n   \n") == []

    def test_handles_pockets_without_blank_lines(self) -> None:
        """fpocket's actual output sometimes runs blocks together
        without blank lines. The parser shouldn't depend on them."""
        compact = (
            "Pocket 1 :\n\tScore : 0.5\n\tVolume : 100\nPocket 2 :\n\tScore : 0.3\n\tVolume : 50\n"
        )
        result = _parse_info_txt(compact)
        assert len(result) == 2
        assert result[0]["Score"] == "0.5"
        assert result[1]["Volume"] == "50"

    def test_ignores_lines_outside_a_pocket_block(self) -> None:
        """Leading or trailing junk that fpocket might emit (banners,
        version info, etc.) shouldn't yield phantom pockets."""
        text = "fpocket version 4.0\nSome banner text\nPocket 1 :\n\tScore : 0.5\n\tVolume : 100\n"
        result = _parse_info_txt(text)
        assert len(result) == 1


class TestMaybeFloat:
    def test_valid_float(self) -> None:
        assert _maybe_float("3.14") == 3.14

    def test_integer_string(self) -> None:
        assert _maybe_float("21") == 21.0

    def test_none_input(self) -> None:
        assert _maybe_float(None) is None

    def test_non_numeric_returns_none(self) -> None:
        # Whatever fpocket might emit for a missing/unknown value.
        assert _maybe_float("N/A") is None
        assert _maybe_float("") is None
        assert _maybe_float("inf garbage") is None

    def test_actual_inf(self) -> None:
        # Python's float() accepts "inf" — that's not a bug;
        # documenting the behaviour here.
        assert _maybe_float("inf") == float("inf")


