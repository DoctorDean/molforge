@pytest.mark.skipif(shutil.which("fpocket") is None, reason="fpocket binary not installed")
class TestRealFpocket:
    """Run the real fpocket binary against a small real structure.
    Skipped in CI by default since fpocket isn't pip-installable.
    Run locally with fpocket on $PATH to exercise the full pipeline."""

    def test_detects_at_least_one_pocket_on_ubiquitin(self, tmp_path: Path) -> None:
        from molforge.io import fetch

        protein = fetch("1UBQ")
        pockets = detect_pockets(protein)
        # 1UBQ is a small protein but fpocket should find a few
        # pockets (the canonical Ile44 patch and a couple more).
        assert len(pockets) >= 1

        top = pockets[0]
        assert isinstance(top, Pocket)
        assert top.center.shape == (3,)
        assert not np.isnan(top.center).any()
        assert top.score is not None
        # Provenance attached.
        assert mk.PROVENANCE in top.metadata
