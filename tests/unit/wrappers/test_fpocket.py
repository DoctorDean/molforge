class TestParseFpocketOutput:
    """Build a synthetic fpocket output directory and parse it.
    Exercises the full _parse_fpocket_output path including the
    vert.pqr / atm.pdb side-files and Provenance attachment."""

    @pytest.fixture
    def synthetic_out_dir(self, tmp_path: Path) -> Path:
        out_dir = tmp_path / "structure_out"
        (out_dir / "pockets").mkdir(parents=True)
        (out_dir / "structure_info.txt").write_text(_REAL_FPOCKET_INFO_BLOCK)
        # Only provide vert.pqr and atm.pdb for the first pocket;
        # the parser should still produce 3 pockets, with the
        # missing-file ones having NaN center and empty residues.
        (out_dir / "pockets" / "pocket0_vert.pqr").write_text(_REAL_VERT_PQR)
        (out_dir / "pockets" / "pocket0_atm.pdb").write_text(_REAL_POCKET_ATM_PDB)
        return out_dir

    def test_returns_three_pockets(self, synthetic_out_dir: Path) -> None:
        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=_minimal_protein(),
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        assert len(pockets) == 3

    def test_first_pocket_has_center_from_vert_pqr(self, synthetic_out_dir: Path) -> None:
        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=_minimal_protein(),
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        # vert.pqr has alpha spheres at (10,20,30), (11,21,31),
        # (12,22,32); the centre is the mean.
        np.testing.assert_array_almost_equal(
            pockets[0].center, np.array([11.0, 21.0, 31.0], dtype=np.float32)
        )

    def test_first_pocket_has_lining_residues(self, synthetic_out_dir: Path) -> None:
        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=_minimal_protein(),
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        # The atm.pdb fixture has ALA 17 and GLY 18, both chain A.
        assert pockets[0].residues == [("A", 17, ""), ("A", 18, "")]

    def test_descriptor_extraction(self, synthetic_out_dir: Path) -> None:
        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=_minimal_protein(),
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        # Pocket 1's headline descriptors from the fixture.
        assert pockets[0].score == 0.490
        assert pockets[0].druggability == 0.019
        assert pockets[0].volume == 270.934
        # rank is 0-indexed; pocket 1 in info.txt -> rank 0.
        assert pockets[0].rank == 0
        # The full descriptor dict is preserved in metadata.
        assert pockets[0].metadata["descriptors"]["Polar SASA"] == "7.611"

    def test_missing_side_files_yield_nan_center(self, synthetic_out_dir: Path) -> None:
        """Pockets 1 and 2 have no vert.pqr / atm.pdb in this fixture
        (only pocket0_*.* was created). They should still appear in
        the output with NaN centre and empty residue list — the
        descriptors from info.txt are the source of truth and shouldn't
        be discarded just because a side-file is absent."""
        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=_minimal_protein(),
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        assert np.isnan(pockets[1].center).all()
        assert pockets[1].residues == []
        # The headline descriptors still came through.
        assert pockets[1].score == 0.320
        assert pockets[1].volume == 150.000

    def test_provenance_attached_and_shared(self, synthetic_out_dir: Path) -> None:
        """All pockets from one call share the same Provenance
        object — same pattern as ProteinMPNN's DesignedSequence
        (frozen + immutable, by-reference sharing is safe)."""
        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=_minimal_protein(),
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        provs = [p.metadata[mk.PROVENANCE] for p in pockets]
        # All three reference the SAME Provenance object.
        assert provs[0] is provs[1] is provs[2]
        # Right engine name + parameters + inputs.
        assert provs[0].engine == "fpocket"
        assert "fpocket_executable" in provs[0].parameters
        assert "min_alpha_spheres" in provs[0].parameters
        assert "protein" in provs[0].inputs

    def test_provenance_chains_to_input_protein(self, synthetic_out_dir: Path) -> None:
        """When the input Protein has its own Provenance (e.g. from
        ESMFold), fpocket's chains back through it — the headline
        cross-engine scenario."""
        protein = _minimal_protein()
        protein.metadata[mk.PROVENANCE] = Provenance.from_engine(
            engine="ESMFold",
            parameters={"model_name": "esmfold_v1"},
            inputs={"sequence": "AAA"},
        )

        pockets = _parse_fpocket_output(
            synthetic_out_dir,
            protein=protein,
            fpocket_executable="fpocket",
            min_alpha_spheres=None,
            min_volume=None,
        )
        chain = pockets[0].metadata[mk.PROVENANCE].chain()
        engines = [step.engine for step in chain]
        assert engines == ["ESMFold", "fpocket"]


