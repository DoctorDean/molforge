class TestDetectPocketsErrors:
    def test_missing_binary_raises_friendly_error(self) -> None:
        protein = _minimal_protein()
        with pytest.raises(FpocketNotInstalledError) as exc:
            detect_pockets(protein, fpocket_executable="/nonexistent/fpocket")
        # The error message points at install guidance.
        msg = str(exc.value)
        assert "fpocket binary not found" in msg
        # Mentions at least one install path so users don't have to
        # grep docs.
        assert any(s in msg for s in ("brew", "apt", "github"))


