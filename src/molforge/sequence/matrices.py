_MATRICES: dict[str, NDArray[np.int_]] = {
    "BLOSUM62": BLOSUM62,
    "PAM250": PAM250,
}


def get_matrix(name: str) -> tuple[NDArray[np.int_], dict[str, int]]:
    """Return ``(matrix, index_map)`` for a named substitution matrix.

    Raises:
        KeyError: If ``name`` is not a known matrix.
    """
    upper = name.upper()
    if upper not in _MATRICES:
        raise KeyError(f"unknown substitution matrix {name!r}; available: {sorted(_MATRICES)}")
    return _MATRICES[upper], dict(_ALPHABET_INDEX)


def available_matrices() -> list[str]:
    """List the named substitution matrices that ship with molforge."""
    return sorted(_MATRICES)
